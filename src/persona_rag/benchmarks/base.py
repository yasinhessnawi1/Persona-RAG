"""Schemas + Protocol shared by every benchmark loader.

Each benchmark turns into a list of ``BenchmarkConversation`` objects. The
runner replays user turns through the mechanism under test; for probe-
carrying conversations, it also augments the knowledge store at the
designated turn (Type B counterfactual injection).

Field-level invariants are enforced via Pydantic; the runner trusts that
loaders only emit valid objects.
"""

from __future__ import annotations

from pathlib import Path
from typing import Literal

import yaml
from pydantic import BaseModel, ConfigDict, Field, model_validator

ProbeType = Literal["self_fact_challenge", "counterfactual", "constraint_bait"]


class _StrictModel(BaseModel):
    """Reject unknown keys so YAML typos surface as validation errors."""

    model_config = ConfigDict(extra="forbid", str_strip_whitespace=False)


class CounterfactualChunk(_StrictModel):
    """A planted counter-evidence document used by Type B probes.

    Authored as a short markdown file with YAML frontmatter; the runner
    indexes it into the knowledge store immediately before the probe turn,
    then removes it after the response. ``contradicts`` is a free-form
    pointer (``persona_id::worldview[N]`` by convention) so the report can
    explain which claim each chunk targets.
    """

    chunk_id: str = Field(min_length=1, max_length=200)
    persona_id: str = Field(min_length=1, max_length=120)
    contradicts: str = Field(min_length=1, max_length=200)
    text: str = Field(min_length=1, max_length=4000)
    source_label: str = Field(default="counter_evidence", max_length=120)


class DriftProbe(_StrictModel):
    """One probe to fire during a multi-turn conversation.

    ``probe_turn_index`` is 0-indexed into the conversation's user-turn
    list. For ``counterfactual`` probes, ``injected_chunk_id`` must be set;
    for ``self_fact_challenge`` and ``constraint_bait`` it must be ``None``
    (the probe is "just an authored user turn at this index"; the
    distinction matters only for reporting, the runner treats them the
    same as ordinary user turns).
    """

    probe_id: str = Field(min_length=1, max_length=200)
    probe_type: ProbeType
    probe_turn_index: int = Field(ge=0)
    injected_chunk_id: str | None = Field(default=None, max_length=200)
    targets: str | None = Field(default=None, max_length=200)

    @model_validator(mode="after")
    def _check_chunk_only_on_counterfactual(self) -> DriftProbe:
        if self.probe_type == "counterfactual" and not self.injected_chunk_id:
            raise ValueError(
                f"probe {self.probe_id!r}: counterfactual probes require injected_chunk_id"
            )
        if self.probe_type != "counterfactual" and self.injected_chunk_id is not None:
            raise ValueError(
                f"probe {self.probe_id!r}: injected_chunk_id is only valid for "
                "counterfactual probes"
            )
        return self


class BenchmarkConversation(_StrictModel):
    """A multi-turn conversation whose user turns are pre-authored.

    The assistant turns are generated by the mechanism under test during
    evaluation. ``probe`` is optional — single-turn benchmarks (PersonaGym,
    PersonaChat) carry no probe; the custom counterfactual suite always
    does.

    Invariant: if ``probe`` is set, ``probe.probe_turn_index`` must be a
    valid index into ``user_turns``.
    """

    conversation_id: str = Field(min_length=1, max_length=200)
    persona_id: str = Field(min_length=1, max_length=120)
    benchmark: str = Field(min_length=1, max_length=120)
    user_turns: list[str] = Field(min_length=1, max_length=20)
    probe: DriftProbe | None = None
    notes: str | None = Field(default=None, max_length=2000)

    @model_validator(mode="after")
    def _probe_turn_in_bounds(self) -> BenchmarkConversation:
        if self.probe is not None and self.probe.probe_turn_index >= len(self.user_turns):
            raise ValueError(
                f"conversation {self.conversation_id!r}: probe_turn_index "
                f"{self.probe.probe_turn_index} out of range "
                f"(have {len(self.user_turns)} user turns)"
            )
        for i, turn in enumerate(self.user_turns):
            if not turn or not turn.strip():
                raise ValueError(f"conversation {self.conversation_id!r}: user_turns[{i}] is empty")
        return self

    def to_yaml(self, path: Path) -> None:
        """Round-trip helper: serialise this conversation to YAML."""
        path = Path(path)
        path.write_text(
            yaml.safe_dump(self.model_dump(mode="json"), sort_keys=False, allow_unicode=True),
            encoding="utf-8",
        )


