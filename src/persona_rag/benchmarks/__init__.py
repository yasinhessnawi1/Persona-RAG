"""Benchmark loaders + probe schemas for the Spec-09 evaluation surface.

Three benchmarks ship in this package:

- ``counterfactual_probes`` — the project's novel benchmark. Multi-turn
  conversations whose user turns are hand-authored; assistant turns are
  generated by each mechanism during evaluation. Three probe types:
  ``self_fact_challenge``, ``counterfactual``, ``constraint_bait``.
- ``personagym`` — vendored from https://github.com/vsamuel2003/PersonaGym (MIT).
  Free-form persona strings → typed ``Persona`` via a documented, lossy
  rule-based mapper. Single-turn questions across five evaluation tasks.
- ``personachat`` — Hugging Face ``bavard/personachat_truecased`` (trait
  personas → ``self_facts``). Single-turn slice for comparability with the
  legacy ConvAI2 numbers.

The ``Benchmark`` Protocol is the common surface; concrete loaders return
``Persona`` plus a list of ``BenchmarkConversation`` objects. Conversations
optionally carry a ``DriftProbe`` for the runner's probe-injection logic.
"""

